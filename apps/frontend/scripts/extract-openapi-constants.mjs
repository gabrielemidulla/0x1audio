#!/usr/bin/env node
/**
 * Emits app/client/constants.gen.ts from openapi.json → x-ox1audio-constants.
 * Runtime values only — types come from hey-api (types.gen.ts) or typeof inference.
 */
import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const root = join(dirname(fileURLToPath(import.meta.url)), "..")
const openapiPath = join(root, "openapi.json")
const outPath = join(root, "app/client/constants.gen.ts")

const doc = JSON.parse(readFileSync(openapiPath, "utf8"))
const constants = doc["x-ox1audio-constants"]
if (!constants || typeof constants !== "object" || Array.isArray(constants)) {
  console.error("openapi.json missing x-ox1audio-constants object")
  process.exit(1)
}

function toScreamingSnake(key) {
  return key.replace(/[A-Z]/g, (ch) => `_${ch}`).toUpperCase()
}

const aliases = Object.keys(constants)
  .map((key) => {
    const name = toScreamingSnake(key)
    return `export const ${name} = OX1AUDIO_CONSTANTS.${key}`
  })
  .join("\n")

const body = `// This file is auto-generated by scripts/extract-openapi-constants.mjs
// Do not edit. Source: openapi.json → x-ox1audio-constants
// Types: use ~/client types.gen (hey-api) or typeof on these consts.

export const OX1AUDIO_CONSTANTS = ${JSON.stringify(constants, null, 2)} as const

${aliases}
`

writeFileSync(outPath, body)
console.log(`Wrote ${outPath}`)
